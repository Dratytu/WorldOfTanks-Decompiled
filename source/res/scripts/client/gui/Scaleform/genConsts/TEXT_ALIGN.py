# This module defines a class for text alignment constants used in GUI scripts.
# These constants are used to specify how text should be aligned within a given
# graphical user interface (GUI) element.

class TEXT_ALIGN(object):
    # The LEFT constant indicates that text should be aligned to the left
    # margin of the GUI element.
    LEFT = 'left'

    # The RIGHT constant indicates that text should be aligned to the right
    # margin of the GUI element.
    RIGHT = 'right'

    # The CENTER constant indicates that text should be centered within the
    # GUI element.
    CENTER = 'center'
